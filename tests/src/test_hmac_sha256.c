// Reference: https://datatracker.ietf.org/doc/html/rfc4231#page-4

static void hmac_sha256_get_hash_rfc4231_test_case_1_success(void** state)
{
    (void)state;

    uint8_t key_raw[] = { 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b };
    uint8_t data_raw[] = { 0x48, 0x69, 0x20, 0x54, 0x68, 0x65, 0x72, 0x65 }; // "Hi There"
    uint8_t expected_hash[] = { 0xb0, 0x34, 0x4c, 0x61, 0xd8, 0xdb, 0x38, 0x53, 0x5c, 0xa8, 0xaf, 0xce, 0xaf, 0x0b, 0xf1, 0x2b, 0x88, 0x1d, 0xc2, 0x00, 0xc9, 0x83, 0x3d, 0xa7, 0x26, 0xe9, 0x37, 0x6c, 0x2e, 0x32, 0xcf, 0xf7 };

    span_t buffer = span_from_memory(buffer_raw);
    span_t key = span_from_memory(key_raw);
    span_t data = span_from_memory(data_raw);;
    span_t hash;

    assert_int_equal(0, hmac_sha256_get_hash(key, data, buffer, &hash));
    assert_int_equal(32, span_get_size(hash));
    assert_memory_equal(expected_hash, span_get_ptr(hash), span_get_size(hash));
}

